#' Australian Marriage Law Postal Survey Participation Data (2017)
#'
#' Participation results from the Australian Marriage Law Postal Survey conducted
#' under the Census and Statistics Act 1905. The survey asked one question:
#' *should the law be changed to allow same-sex couples to marry?* Data presents
#' results at the territory/state level.
#'
#' `abs_partp2017` displays the 2017 participation of an Australian age bracket
#' in a State/Territory and 5 variables are collected per unit:
#' "State/Territory", "age group", "total participants", "eligible participants",
#' and "participation rate". The current arrangement of the data is unsuitable
#' for analysis.
#'
#' Eligible participants are those enrolled on the Commonwealth Electoral
#' Roll, or those who made a legitimate application for enrollment, and were
#' eligible to vote in a federal election as of 24 August 2017.
#'
#'
#' @source Australian Bureau of Statistics (2017) 'Australian Marriage Law Postal Survey 2017 - Participation', *Australian Marriage Law Postal Survey, 2017*, accessed 21 November 2022.
#' @source <https://www.abs.gov.au/ausstats/abs@.nsf/mf/1800.0>
